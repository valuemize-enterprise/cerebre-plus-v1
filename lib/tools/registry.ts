export const TOOL_MAP = new Map<string, ToolDefinition>(
  TOOL_REGISTRY.map(t => [t.id, t])
)

export function getTool(id: string): ToolDefinition | undefined {
  return TOOL_MAP.get(id)
}

export function getToolsByCategory(category: ToolCategory): ToolDefinition[] {
  return TOOL_REGISTRY.filter(t => t.category === category)
}

export function getNextTools(toolId: string): ToolDefinition[] {
  const tool = getTool(toolId)
  if (!tool) return []
  return tool.nextToolIds
    .map(id => getTool(id))
    .filter((t): t is ToolDefinition => t !== undefined)
}

export function canAffordTool(toolId: string, balance: number): boolean {
  const tool = getTool(toolId)
  return !!tool && balance >= tool.coinCost
}

